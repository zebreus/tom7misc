
#include "tls.h"

#include <initializer_list>

#include "ansi.h"
#include "base/logging.h"
#include "base/print.h"
#include "packet-parser.h"

#define CHECK_SOME(opt) ([&]{                          \
      auto o = (opt);                                  \
      CHECK(o.has_value()) << #opt << " was nullopt."; \
      return o.value();                                \
    }())

static void TestParseClientHello() {
  // Example ClientHello from tls12.xargs.org
  static constexpr uint8_t EXAMPLE[] = {
    // TLS Record header
    // 0x16, 0x03, 0x01, 0x00, 0xa5,
    // ClientHello
    0x01, 0x00, 0x00, 0xa1, 0x03, 0x03, 0x00, 0x01, 0x02, 0x03, 0x04,
    0x05, 0x06, 0x07, 0x08, 0x09, 0x0a, 0x0b, 0x0c, 0x0d, 0x0e, 0x0f,
    0x10, 0x11, 0x12, 0x13, 0x14, 0x15, 0x16, 0x17, 0x18, 0x19, 0x1a,
    0x1b, 0x1c, 0x1d, 0x1e, 0x1f, 0x00, 0x00, 0x20, 0xcc, 0xa8, 0xcc,
    0xa9, 0xc0, 0x2f, 0xc0, 0x30, 0xc0, 0x2b, 0xc0, 0x2c, 0xc0, 0x13,
    0xc0, 0x09, 0xc0, 0x14, 0xc0, 0x0a, 0x00, 0x9c, 0x00, 0x9d, 0x00,
    0x2f, 0x00, 0x35, 0xc0, 0x12, 0x00, 0x0a, 0x01, 0x00, 0x00, 0x58,
    0x00, 0x00, 0x00, 0x18, 0x00, 0x16, 0x00, 0x00, 0x13, 0x65, 0x78,
    0x61, 0x6d, 0x70, 0x6c, 0x65, 0x2e, 0x75, 0x6c, 0x66, 0x68, 0x65,
    0x69, 0x6d, 0x2e, 0x6e, 0x65, 0x74, 0x00, 0x05, 0x00, 0x05, 0x01,
    0x00, 0x00, 0x00, 0x00, 0x00, 0x0a, 0x00, 0x0a, 0x00, 0x08, 0x00,
    0x1d, 0x00, 0x17, 0x00, 0x18, 0x00, 0x19, 0x00, 0x0b, 0x00, 0x02,
    0x01, 0x00, 0x00, 0x0d, 0x00, 0x12, 0x00, 0x10, 0x04, 0x01, 0x04,
    0x03, 0x05, 0x01, 0x05, 0x03, 0x06, 0x01, 0x06, 0x03, 0x02, 0x01,
    0x02, 0x03, 0xff, 0x01, 0x00, 0x01, 0x00, 0x00, 0x12, 0x00, 0x00,
  };

  TLS::ClientHello hello =
    CHECK_SOME(TLS::ParseClientHello(PacketParser(EXAMPLE)));

  for (int i = 0; i < 32; i++) {
    // The example packet uses a counting sequence.
    CHECK(hello.client_random[i] == i);
  }

  bool had_sni = false;
  for (const TLS::ClientHello::Extension &ext : hello.extensions) {
    if (const TLS::ServerNameIndication *sni =
        std::get_if<TLS::ServerNameIndication>(&ext)) {
      CHECK(!had_sni) << "Multiple SNI extensions?";
      CHECK(sni->hosts.size() == 1);

      had_sni = true;
    }
  }

  CHECK(had_sni) << "Didn't parse SNI extension";
}

int main(int argc, char **argv) {
  ANSI::Init();

  TestParseClientHello();

  Print("OK\n");
  return 0;
}
