std::string_view JohnsonSolidName(int n) {
  CHECK(n >= 1 && n <= 92) << "Johnson solids are numbered 1..92. Got: " << n;
  return NAMES[n - 1];
}

Polyhedron JohnsonSolid(int n) {
  CHECK(n >= 1 && n <= 92) << "Johnson solids are numbered 1..92. Got: " << n;

  std::vector<vec3> verts(VERTICES[n - 1]);
  std::optional<Polyhedron> opoly = PolyhedronFromConvexVertices(
      std::move(verts), JohnsonSolidName(n));
  CHECK(opoly.has_value()) << "Failed for J" << n;

  return std::move(opoly.value());
}
